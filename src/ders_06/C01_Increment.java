package ders_06;

public class C01_Increment {
    public static void main(String[] args) {

        int number = 10;

        System.out.println(number = number + 3); //13

        System.out.println(number); //13

        System.out.println(number -= 5); //8

        System.out.println(number); //8

        System.out.println(number++); //8

        System.out.println(number); // 9


    }
}
